class DefaultCase
{
	public static void main(String... args){
		int x = 3;
		switch (x)
		{
			default:
			        System.out.println("def");
			case 0:
			       System.out.println("0");
				   break;
			case 1:
			       System.out.println("1");
			case 2:
			       System.out.println("2");
		}
	}
}

				   